// using template literals

console.log("Example of template literals for the stationary shop");

let item1 = "Pen";
let item2 = "Pencil";
let item3 = "Eraser";

let price1 = 10;
let price2 = 5;
let price3 = 2;

console.log(`The price of ${item1} is ${price1}`);
console.log(`The price of ${item2} is ${price2}`);
console.log(`The price of ${item3} is ${price3}`);

console.log(`The total price of ${item1}, ${item2} and ${item3} is ${price1 + price2 + price3}`);